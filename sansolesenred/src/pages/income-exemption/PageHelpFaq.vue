              </li>
              <li>
                cittadini titolari o a carico di altro soggetto titolare, di assegno (ex pensione) sociale, (codice
                E03);
              </li>
              <li>
                cittadini titolari o a carico di altro soggetto titolare, di pensione al minimo, con più di 60 anni e
                reddito familiare inferiore a 8.263,31 euro, incrementato a 11.362,05 euro in presenza del coniuge ed in
                ragione di ulteriori 516,46 euro per ogni figlio a carico, (codice E04);
              </li>
              Per i cittadini in possesso del certificato di esenzione con il codice E02, disoccupati e lavoratori in
              mobilità, Regione ha previsto la proroga straordinaria sino al <strong>29 Ottobre 2020</strong>
            </ul>
