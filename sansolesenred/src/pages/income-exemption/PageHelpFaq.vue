          label="1.  Che cos'è l'Esenzione per Reddito?">
          <div>
            L'esenzione ticket in base al reddito è un diritto che spetta ai cittadini (e al loro nucleo famigliare) con
            un reddito familiare fiscale inferiore alla soglia limite prevista e fissata dall'attuale normativa.
          </div>
        </q-collapsible>
        <q-collapsible
          sparse
          label="2.  Che cos’è l’attestato di esenzione ticket in base al reddito?">
          <div>
            È un certificato, rilasciato dall’Azienda ASL, in cui sono registrati: i dati anagrafici dell’interessato,
            il codice fiscale, il codice di esenzione, il numero del certificato e la data di scadenza. Viene rilasciato
            a seguito di autocertificazione firmata dalla persona interessata e consegnata all’Azienda ASL (anche
            attraverso la modalità telematica). Con l'autocertificazione la persona dichiara, sotto la propria
            responsabilità, di essere in possesso dei requisiti di reddito e delle altre condizioni previste che danno
            diritto ad usufruire dell’esenzione.
          </div>
        </q-collapsible>
        <q-collapsible
          sparse
          label="3.  Ho i requisiti per fare l'autocertificazione Esenzione per Reddito?">
          <div>
            Per ottenere l'esenzione ticket per reddito il cittadino deve avere un reddito fiscale complessivo del
            nucleo familiare, dato dalla somma dei redditi prodotti dai singoli componenti del nucleo nel corso
            dell'anno precedente, al lordo degli oneri deducibili, inferiore alla soglia limite prevista e fissata
            dall'attuale normativa. Vanno presi in considerazione anche i redditi da fabbricati, ivi compresa
            l'abitazione principale e la cedolare secca. Si ricorda inoltre che il reddito del coniuge non legalmente
            separato concorre sempre alla determinazione del reddito complessivo del nucleo familiare.
          </div>
        </q-collapsible>
        <q-collapsible
          sparse
          label="4.  Come si ottiene, in sintesi, l’esenzione ticket in base al reddito?">
          <div>
            Per ottenere l’esenzione dal pagamento del ticket in base al reddito, la persona interessata deve chiedere
            alla propria ASL il rilascio dell’apposito “Attestato di esenzione dal pagamento del ticket in base al
            reddito”. L’attestato di esenzione viene rilasciato a seguito di autocertificazione del possesso dei
            requisiti necessari. L'autocertificazione può essere resa dalla persona interessata presso gli sportelli
            della propria Azienda ASL (quella in cui è stata effettuata la scelta del medico di famiglia), oppure
            tramite il servizio on line messo a disposizione dalla Regione Piemonte. Con l'autocertificazione il
            cittadino si assume la responsabilità delle sue dichiarazioni. Il diritto all’esenzione viene esercitato
            dall’interessato al momento della prescrizione di visite ed esami dinanzi al proprio medico di famiglia o
            allo specialista che provvederanno ad indicarlo sull’impegnativa.
          </div>
        </q-collapsible>
        <q-collapsible
          sparse
          label="5.  Chi ha diritto a fare richiesta di autocertificazione Esenzione per Reddito?">
          <div>
            Hanno diritto all'esenzione ticket per reddito:
            <ul>
              <li>
                Bambini di età inferiore a 6 anni e anziani over 65 anni, purché appartenenti ad un nucleo familiare con
                reddito complessivo non superiore a 36.151,98 euro l'anno (codice E01)
              </li>
              <li>
                Disoccupati e familiari a carico: se il reddito è inferiore a 8.263,31 euro aumentabile a 11.362,05 euro
                nel caso di coniuge a carico, + ulteriori € 516,46 per ogni figlio a carico (codice E02):
              </li>
              <li>Percettori di assegno sociale e familiari a carico (codice E03)</li>
              <li>
                Titolari di pensione minima: di età superiore a 60 anni e familiari a carico, appartenenti ad un nucleo
                familiare con reddito complessivo inferiore a 8.263,31 euro, ad euro 11.362,05 se è presente anche il
                coniuge a carico, più ulteriori 516,46 euro per ogni figlio a carico (codice E04).
              </li>
              <li>
                Cittadini di età inferiore o uguale a 18 anni e superiore o uguale a 55 anni, appartenenti ad un nucleo
                familiare rilevante a fini fiscali (e non anagrafici):
                <ul>
                  <li>sino a tre componenti con reddito annuo complessivo non superiore a €44.000,00</li>
                  <li>di quattro componenti con reddito annuo complessivo non superiore a €49.000,00</li>
                  <li>di almeno cinque componenti con reddito annuo complessivo non superiore a €54.000,00.</li>
                </ul>
                (codice E10)
              </li>
            </ul>
          </div>
        </q-collapsible>
        <q-collapsible
          sparse
          label="6.  Da chi può essere resa l’autocertificazione?">
          <div>
            Ai sensi degli artt. 46 e 47 del DPR 445/2000 reddito, l'autocertificazione è resa dall’interessato e può
            essere resa dai seguenti soggetti
            <ul>
              <li>
                dal genitore che esercita la potestà, se l’interessato è soggetto alla potestà dei genitori
              </li>
              <li> dal tutore se l’interessato è soggetto a tutela</li>
              <li> dall’amministratore di sostegno se l’interessato ne è soggetto</li>
              <li>
                dall’interessato, con l’assistenza del curatore se l’interessato è soggetto a curatela.
              </li>
              <li>
                dal coniuge o, in sua assenza, dai figli o, in mancanza, da altro parente in linea retta
                o collaterale fino al 3°grado, per impedimento temporaneo per motivi di salute.
              </li>
            </ul>
          </div>
        </q-collapsible>
        <q-collapsible
          sparse
          label="7.  Per il rilascio dell’attestato di esenzione ticket mi devo rivolgere esclusivamente all’Azienda ASL di residenza?">
          <div>
            Per il rilascio del certificato di esenzione ci si deve rivolgere alla propria Azienda ASL, che è quella in
            cui è stata effettuata la scelta del medico di famiglia. La propria Azienda ASL può anche essere in una
            provincia o in una regione differente rispetto al luogo di residenza, come accade per le persone domiciliate
            in un comune diverso da quello di residenza, ad esempio per studio per lavoro o per salute. E, ovviamente,
            OnLine tramite ESENRED (Esenzione per Reddito), collegandosi al sito Sistema Piemonte.
          </div>
        </q-collapsible>
        <q-collapsible
          sparse
          label="8.  Quale documentazione devo presentare per chiedere il certificato di esenzione? ">
          <div>
            È sufficiente presentare la propria autocertificazione, firmata personalmente (o dal genitore di
            minorenne/tutore), su apposito documento fornito dall'ASL. In questa autocertificazione, la persona dichiara
            di essere in possesso dei requisiti che danno diritto all’esenzione e si impegna a chiedere l’annullamento
            del certificato di esenzione qualora si rendesse conto di non essere più in possesso dei requisiti
            dichiarati. Non occorre allegare al modulo di autocertificazione alcuna documentazione. Le stesse regole
            valgono per l'autocertificazione on line.
          </div>
        </q-collapsible>
        <q-collapsible
          sparse
          label="9.  Ho già l’esenzione dal ticket per invalidità (o per patologia), posso chiedere anche l’esenzione per reddito, avendone i requisiti? ">
          <div>
            Non c’è incompatibilità a priori tra esenzione per invalidità/patologia ed esenzione per reddito e quando ci
            sono i requisiti la persona potrebbe avere diritto ad entrambe le esenzioni. Se l’esenzione per invalidità è
            estesa a tutte le visite ed esami, il certificato di esenzione in base al reddito non aggiunge alcun
            ulteriore beneficio. Se invece l’esenzione per invalidità esenta dal pagamento del ticket solo le
            visite/esami collegati alla specifica invalidità (così come avviene per l’esenzione per patologia), può
            servire chiedere anche l’esenzione per reddito, che sarà utile per le altre prestazioni sanitarie. In
            presenza di esenzione per reddito ed anche di esenzione per invalidità/patologia, il medico registrerà nella
            ricetta medica di prescrizione di visite/esami il codice di esenzione più favorevole in quel caso per la
            persona interessata.
          </div>
        </q-collapsible>
        <q-collapsible
          sparse
          label="10.  Qual è la durata dell'esenzione per reddito? ">
          <div>
            L'esenzione ha, normalmente, durata pari ad un anno. All'atto pratico la sua durata va dal momento della
            sottoscrizione al 31 Marzo (dell'anno in corso per autocertificazioni inserite dal 1° gennaio al 30 marzo o
            dell'anno successivo per autocertificazioni inserite dal 1° aprile al 31 dicembre)
          </div>
        </q-collapsible>
        <q-collapsible
          sparse
          label="11.  Come posso sapere qual è il mio reddito dell’anno precedente se non ho ancora presentato
              la dichiarazione dei redditi o ricevuto il CUD o altro analogo modello?  ">
          <div>
            Nei primi mesi dell’anno, quando non si è ancora in possesso di informazioni documentate sul reddito
            percepito nell’anno precedente, si dovrà compilare l’autocertificazione sulla base del reddito che si valuta
            di avere effettivamente percepito, anche basandosi sulla precedente dichiarazione dei redditi, se la
            situazione non è modificata. Non appena in possesso della documentazione necessaria, si dovrà verificare se
            il proprio reddito rientra nei limiti previsti. Nel caso in cui questi limiti siano superati, si dovrà
            immediatamente darne comunicazione all’Azienda ASL pagare gli eventuali ticket non versati e riconsegnare
            l’attestato di esenzione.
          </div>
        </q-collapsible>
        <q-collapsible
          sparse
          label="12.  Come si determina il nucleo familiare fiscale?  ">
          <div>
            Come previsto dal DM 22 gennaio 1993, ai fini dell'esenzione dalla compartecipazione alla spesa sanitaria,
            il nucleo familiare fiscale è costituito dall'interessato, dal coniuge non legalmente separato e dagli altri
            familiari fiscalmente a carico. Si fa riferimento alla composizione del nucleo familiare relativo all'anno
            precedente. È assolutamente irrilevante il fatto che i diversi componenti convivano. Le persone a carico
            sono coloro per le quali spettano le detrazioni per carichi di famiglia in quanto titolari di un reddito
            annuo lordo non superiore ad euro 2.840,51. I soggetti conviventi ma fiscalmente autonomi costituiscono un
            distinto nucleo familiare a fini fiscali. Sono considerati a carico:
            <ul>
              <li> il coniuge non legalmente ed effettivamente separato</li>
              <li> i figli, anche se naturali riconosciuti, adottivi, affidati o affiliati, senza limiti
                di età anche se non conviventi
              </li>
            </ul>
            Sono inoltre individuati quali "altri familiari a carico", i seguenti familiari
            conviventi con reddito non superiore a quello sopracitato:
            <ol type="a">
              <li> il coniuge legalmente ed effettivamente separato</li>
              <li> i discendenti dei figli</li>
              <li> i genitori e gli ascendenti prossimi, anche naturali</li>
              <li> i genitori adottivi</li>
              <li> i generi e le nuore</li>
              <li> il suocero e la suocera</li>
              <li> i fratelli e le sorelle, anche unilaterali</li>
            </ol>
          </div>
        </q-collapsible>
        <q-collapsible
          sparse
          label="13.  Il minore di anni sei con genitori non coniugati a quale nucleo fiscale appartiene?">
          <div>
            Il minore di anni sei appartiene al nucleo fiscale dei genitori se coniugati. In caso di genitori
            conviventi, il minore di anni sei appartiene a nucleo fiscale del genitore cui il minore è fiscalmente a
            carico. Nel caso in cui il bambino sia fiscalmente a carico di entrambi i genitori, è possibile scegliere la
            situazione più favorevole, vale a dire considerare il bambino appartenente al nucleo del genitore il cui
            reddito non supera il limite previsto (€ 36.151,98).
          </div>
        </q-collapsible>
        <q-collapsible
          sparse
          label="14.  Per il minore in adozione/affido/comunità residenziale chi deve compilare l’autocertificazione?">
          <div>
            Per il minore in adozione/affido/comunità residenziale il modulo di autocertificazione deve essere compilato
            dal soggetto giuridicamente titolare dell’adozione/affidamento del minore: il genitore adottivo o
            affidatario, il responsabile della comunità residenziale, il sindaco ecc...
          </div>
        </q-collapsible>
