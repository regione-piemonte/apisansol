            specialistica poiché, secondo le disposizioni del DM 11/12/2009, è all'atto della prescrizione che si rileva
            il diritto all'esenzione. Pertanto, se tra il momento della compilazione della ricetta e il momento in cui
            si fruisce della prestazione, decade il diritto all'esenzione, non si sarà comunque tenuti al pagamento del
            ticket.
          </div>
        </q-collapsible>
