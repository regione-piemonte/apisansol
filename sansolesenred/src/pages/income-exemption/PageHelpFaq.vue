            esenzione per reddito, già rilasciate dalle Aziende sanitarie locali. Le
            categorie che usufruiscono dell’esenzione sono:
            <ul>
              <li>
                cittadini con meno di 6 o più di 65 anni, con reddito familiare inferiore a 36.151,98 euro, (codice
